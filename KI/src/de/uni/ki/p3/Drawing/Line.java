package de.uni.ki.p3.Drawing;

public class Line {
    private String stroke;
    private double x1;
    private double x2;
    private double y1;
    private double y2;
    
	public String getStroke() {
		return stroke;
	}
	public void setStroke(String stroke) {
		this.stroke = stroke;
	}
	public double getX1() {
		return x1;
	}
	public void setX1(double d) {
		this.x1 = d;
	}
	public double getX2() {
		return x2;
	}
	public void setX2(double d) {
		this.x2 = d;
	}
	public double getY1() {
		return y1;
	}
	public void setY1(double d) {
		this.y1 = d;
	}
	public double getY2() {
		return y2;
	}
	public void setY2(double d) {
		this.y2 = d;
	}
}
